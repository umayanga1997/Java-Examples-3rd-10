package math_class_methods;

public class Main {

    public static void main(String[] args) {
        
        System.out.println("abs is :" + Math.abs(-27));
        System.out.println("ceil is :" + Math.ceil(7.4));
        System.out.println("floor is :" + Math.floor(4.25));
        System.out.println("max is :" + Math.max(10, 20));
        System.out.println("min is :" + Math.min(10, 20));
        System.out.println("pow is :" + Math.pow(5, 4));
        System.out.println("sqrt is :" + Math.sqrt(9));
        
        String Bye = " Bye ";
        
        for (int x=1; x<4;x++){
            System.out.print(Bye);
        }
        
    }
    
}
